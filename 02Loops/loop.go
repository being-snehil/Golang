package main

import "fmt"
func main(){
	// syntax for while loop in GO
	i := 0
	for i < 5 {
		fmt.Println(i)
		i++
	}
	// syntax for  for loop in GO
		for i := 0; i < 5; i++ {
                    fmt.Println(i)
          }

	// syntax for infinite loop in GO
	for {
		println("infinite loop")
	}

	

}